import java.util.ArrayList;
public class ProducerConsumer {
    public static void main(String[] args) {
        TaskFactoryProdCon taskFactory = new TaskFactoryProdCon();
        ArrayList<Double> list = new ArrayList<>(20);
        Thread prod = new Thread(new TaskProdCon("producer", taskFactory, list));
        prod.setName("producer thread");
        Thread con = new Thread(new TaskProdCon("consumer", taskFactory, list));
        con.setName("consumer thread");
        prod.start();
        con.start();
    }
}
class TaskProdCon implements Runnable{
    private String role;
    TaskFactoryProdCon taskFactory;
    ArrayList<Double> list;
    public TaskProdCon(String role, TaskFactoryProdCon taskFactory, ArrayList<Double> list) {
        this.role = role;
        this.taskFactory = taskFactory;
        this.list = list;
    }
    @Override
    public void run() {
        int count = 0;
        while(count++ < 500){
            if("producer".equals(role))
                taskFactory.produce(list);
            else
                taskFactory.consume(list);
        }
    }
}
class TaskFactoryProdCon{
    public void produce(ArrayList<Double> list){
        synchronized(this){
            while(list.size() >= 20){
                try {
                    System.out.print(Thread.currentThread().getName() + " is waiting to produce -> ");
                    wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
            Double num = Math.random()*10;
            list.add(num);
            System.out.println(Thread.currentThread().getName() + ", adding " + num + " in buffer. Buffer size : " + list.size());
            notifyAll();
        }
    }
    public void consume(ArrayList<Double> list){
        synchronized(this){
            while(list.size() < 1){
                try {
                    wait();
                } catch (InterruptedException e) {
                    e.printStackTrace();
                }
            }
            Double num = list.remove(0);
            System.out.println(Thread.currentThread().getName() + ", removing " + num + " from buffer. Buffer size : " + list.size());
            notifyAll();
        }
    }
}
